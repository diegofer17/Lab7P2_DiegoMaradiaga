    private static String generarCadena10(){
        int length = 10; 
        int lBound = 48; 
        int uBound = 122; 
        
        Random r = new Random(); 
        String cadena = "";
        
        for (int i = 0; i < length; i++) {
            int v = lBound + r.nextInt(uBound - lBound + 1);
            if ((v > 47 && v <= 57) || (v > 64 && v <= 90) || (v > 96 && v <= 122)) {
                char randomChar = (char) v;
                cadena += randomChar;
            }else{
                i=i-1;
            }
        }
        
        return cadena;
    }
    
